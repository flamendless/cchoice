package main

import (
	"crypto/sha256"
	"encoding/hex"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"sort"
)

var assetDirs = []string{
	"cmd/web/static/js",
	"cmd/web/static/css",
	"cmd/web/static/images/favicons",
}

func repoRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found")
		}
		dir = parent
	}
}

func main() {
	root, err := repoRoot()
	if err != nil {
		fmt.Fprintf(os.Stderr, "failed to find repo root: %v\n", err)
		os.Exit(1)
	}

	dirs := make([]string, len(assetDirs))
	for i, dir := range assetDirs {
		dirs[i] = filepath.Join(root, dir)
	}

	version, err := hashAssetDirs(dirs)
	if err != nil {
		fmt.Fprintf(os.Stderr, "failed to hash static assets: %v\n", err)
		os.Exit(1)
	}
	if version == "" {
		version = "dev"
	}

	content := fmt.Sprintf(`// Code generated by go:generate; DO NOT EDIT.
package utils

func init() {
    assetVersion = %q
}
`, version)

	outPath := "assets_gen.go"
	if err := os.WriteFile(outPath, []byte(content), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "failed to write %s: %v\n", outPath, err)
		os.Exit(1)
	}
}

func hashAssetDirs(dirs []string) (string, error) {
	var paths []string
	for _, dir := range dirs {
		if err := filepath.Walk(dir, func(path string, info os.FileInfo, err error) error {
			if err != nil {
				return err
			}
			if info.IsDir() {
				return nil
			}
			paths = append(paths, path)
			return nil
		}); err != nil {
			if os.IsNotExist(err) {
				continue
			}
			return "", err
		}
	}

	if len(paths) == 0 {
		return "", nil
	}

	sort.Strings(paths)

	h := sha256.New()
	for _, path := range paths {
		if _, err := io.WriteString(h, path); err != nil {
			return "", err
		}
		f, err := os.Open(path)
		if err != nil {
			return "", err
		}
		if _, err := io.Copy(h, f); err != nil {
			_ = f.Close()
			return "", err
		}
		if err := f.Close(); err != nil {
			return "", err
		}
	}

	return hex.EncodeToString(h.Sum(nil))[:12], nil
}
